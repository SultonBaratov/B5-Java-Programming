package day04_variables;
/*
    Create a class called EscapeSequence
    Create a main method
    Output (result) this exactly:

        Ex 1:
            "iphone", "laptop", "xbox", "webcam", "monitor"

        Ex 2:
            iphone
                laptop
            xbox
                webcam
            monitor
*/
public class EscapeSequence {

    public static void main (String[] args){
        System.out.println("\"iphone\", \"laptop\", \"xbox\", \"webcam\", \"monitor\"");
        // if you copy and past in second line another quoted word it will automatically make word in quotes

        System.out.println("");

        System.out.println("iphone\n\tlaptop\nxbox\n\twebcam\nmonitor");

    }
}
