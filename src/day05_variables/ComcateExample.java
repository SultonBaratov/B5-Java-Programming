package day05_variables;

public class ComcateExample {
    public static void main(String[] args) {
        System.out.println(5 + 4 ); //no Concatenation = 9
        System.out.println(5 + 4 + "Hello"); // Concatenation = 9Hello

        System.out.println("hello" + 5 + 4); // Concatenation = Hello9

    }
}
